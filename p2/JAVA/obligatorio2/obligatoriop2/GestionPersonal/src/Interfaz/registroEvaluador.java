    private void jBCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCancelarActionPerformed
        // TODO add your handling code here:
        Object[] opciones = {"Ok", "Cancelar"};
        String mensaje = "Si cancela se borraran todos los datos ";
        int opcionSeleccionada = JOptionPane.showOptionDialog(this,
                mensaje,
                "Confirmación",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                opciones, opciones[1]);
        if (opcionSeleccionada == 0) {
            txtVacio();

        }

    }//GEN-LAST:event_jBCancelarActionPerformed

    private void jBRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRegistrarActionPerformed
        if (esCedulaValida(jtxtId.getText()) && (Integer.parseInt(jtxtAñoIn.getText()) <= 2023)) {
            setLabelExceptionVisible();
            String nombre = jtxtNombre.getText();
            String id = jtxtId.getText();
            String direccion = jtxtDir.getText();
            String añoIn = jtxtAñoIn.getText();
            setDefaultBorder();
            try {
                modelo.registrarEvaluador(nombre, id, direccion, añoIn);
                txtVacio();
            } catch (Exception_StringVacio ex) {
                Border border = BorderFactory.createLineBorder(Color.RED, 2);
                checkAndSetBorder(border);
                jLabelException.setVisible(true);
                jLabelException.setText("Complete las áreas indicadas");
            } catch (Exception_ParseNumero ex) {
                jLabeExcep1.setText("Debe Ingresar un número");
                jLabeExcep1.setVisible(true);
                jLabelExcep2.setText("Debe Ingresar un número");
                jLabelExcep2.setVisible(true);
            }
        } else {
            if (!esCedulaValida(jtxtId.getText())) {
                JOptionPane.showMessageDialog(null, "Error: Ya hay un evaluador con esa cedula");

            }
            if ((Integer.parseInt(jtxtAñoIn.getText()) <= 2023)) {
                JOptionPane.showMessageDialog(null, "Ingrese una fecha anterior o igual a 2023");
            }

        }
    }//GEN-LAST:event_jBRegistrarActionPerformed
    private void txtVacio() {
        jtxtNombre.setText("");
        jtxtId.setText("");
        jtxtDir.setText("");
        jtxtAñoIn.setText("");
    }

    private void checkAndSetBorder(Border border) {
        if (jtxtNombre.getText().equals("")) {
            jtxtNombre.setBorder(border);
        }
        if (jtxtDir.getText().equals("")) {
            jtxtDir.setBorder(border);
        }
        if (jtxtId.getText().equals("")) {
            jtxtId.setBorder(border);
        }
        if (jtxtAñoIn.getText().equals("")) {
            jtxtAñoIn.setBorder(border);
        }
    }
    
