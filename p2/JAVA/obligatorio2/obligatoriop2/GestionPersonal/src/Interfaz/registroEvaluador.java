    public boolean esCedulaValida(String id) {
        boolean res = true;
        for (int i = 0; i < modelo.getListaEvaluadores().size(); i++) {
            if (String.valueOf(modelo.getListaEvaluadores().get(i).getId()).equals(id)) {
                res = false;
            }
        }
        return res;
    }

