    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed

        try {
            jProgressBarCar.setMinimum(0);
            jProgressBarCar.setMaximum(100);
            jProgressBarCar.setVisible(true);
            jProgressBarCar.setValue(0);
            ObjectInputStream in = new ObjectInputStream(Files.newInputStream(Paths.get("data.txt")));
            Sistema miModelo = (Sistema) in.readObject();
            modelo = miModelo;
            if (miModelo.sistemaEstaVacio()) {
                JOptionPane.showMessageDialog(this, "No hay nada guardado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);

            } else {
                jProgressBarCar.setValue(50);
                this.menu.setModelo(miModelo);
                Menu menu = new Menu(miModelo);
                this.setMenu(menu);
                jProgressBarCar.setValue(100);
                cargarDatos = true;
            }

        } catch (EOFException exEOF) {
            JOptionPane.showMessageDialog(this, "No hay nada guardado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException ex) {
            Logger.getLogger(inicio.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(inicio.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnCargarActionPerformed

