    private void jcbTematicaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbTematicaItemStateChanged
        // TODO add your handling code here:
        Lmodelo.clear();
        jLabelTematica.setText(jcbTematica.getSelectedItem().toString());
        for (int i = 0; i < modelo.getListaPostulantes().size(); i++) {
            boolean flag = false;
            Postulante pos = modelo.getListaPostulantes().get(i);
            for (int j = 0; !flag && j < pos.getListaExperiencia().size(); j++) {
                Experiencia exp = pos.getListaExperiencia().get(j);
                if ((exp.getExperiencia().toString().equalsIgnoreCase(jcbTematica.getSelectedItem().toString())) && (exp.getNivel() >= 5)) {
                    flag = true;
                }
            }
            if (flag) {
                Lmodelo.addElement(pos);
            }
        }
    }//GEN-LAST:event_jcbTematicaItemStateChanged


