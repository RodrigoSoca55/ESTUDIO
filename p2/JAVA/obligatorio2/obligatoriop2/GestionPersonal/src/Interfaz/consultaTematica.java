    public consultaTematica(Sistema miModelo) {
        this.modelo = miModelo;
        initComponents();
        jListPost.setModel(Lmodelo);
        for (Tematica tem : modelo.getListaTematicas()) {
            jcbTematica.addItem(tem);
        }
        Tematica tematIni = (Tematica) jcbTematica.getSelectedItem();
        setearInfo(tematIni);
        jcbTematica.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Tematica tem = (Tematica) jcbTematica.getSelectedItem();
                setearInfo(tem);
                postulantesMayorNivel5();
            }
        });
        
        
    }
    private void setearInfo(Tematica tem){
        jLabelTematica.setText(tem.toString());
        jLabelTematica2.setText(tem.toString() + ": ");
        int cant = modelo.cantPuestosTematica(tem);
        jLabelRespuesta.setText(String.valueOf(cant));
        jLabelDescripcion.setText(tem.getDescripcion());
    }
    private void postulantesMayorNivel5(){
        for (int i = 0; i < modelo.getListaPostulantes().size(); i++) {
            Postulante pos = modelo.getListaPostulantes().get(i);
            if (pos.postulanteTieneExperienciaMayorNivel(5,(Tematica) jcbTematica.getSelectedItem())){
                Lmodelo.addElement(pos);
            }
        }
    }
    
