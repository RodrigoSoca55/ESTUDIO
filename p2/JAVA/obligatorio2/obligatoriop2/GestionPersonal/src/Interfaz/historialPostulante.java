    public DefaultListModel<Postulante> sortLmodelo(DefaultListModel<Postulante> Lmodelo) {
        ArrayList<Postulante> listaPost = modelo.getListaPostulantes();
        Collections.sort(listaPost);
        for (Postulante postulante : listaPost) {
            Lmodelo.addElement(postulante);
        }

        return Lmodelo;
    }

