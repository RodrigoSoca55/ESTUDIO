    private void cargarTablaAlClick() {
        DefaultTableModel modeloTabla = (DefaultTableModel) jTableCont.getModel();
        modeloTabla.setRowCount(0); // Limpiar el modelo de la tabla

        ArrayList<Entrevista> listaEntrevistaPos = jListPostulantes.getSelectedValue().getListaEntrevistas();
        int largoListaEntrevistaPos = listaEntrevistaPos.size();
        Collections.sort(listaEntrevistaPos);

        for (int i = 0; i < largoListaEntrevistaPos; i++) {
            Entrevista entre = listaEntrevistaPos.get(i);
            Object datos[] = new Object[4];
            datos[0] = entre.getId();
            datos[1] = entre.getEvaluador();
            datos[2] = entre.getPuntaje();
            datos[3] = entre.getComentarios();

            modeloTabla.addRow(datos);
        }
    }

