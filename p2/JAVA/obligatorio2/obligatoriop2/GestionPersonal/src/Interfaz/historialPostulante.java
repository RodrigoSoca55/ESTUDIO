    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        resetearColor();
        String texto = jTextFieldBuscar.getText();
        for (int i = 0; i < jTableCont.getRowCount(); i++) {
            String aux = (String) jTableCont.getValueAt(i, 3);
            if (aux.contains(texto)) {
                if (!(aux.indexOf(texto) + texto.length() == aux.length())) {
                    int termina = aux.indexOf(texto) + texto.length();
                    jTableCont.setValueAt("<html><font color=black>" + aux.substring(0, aux.indexOf(texto)) + "<html><font color=red>" + aux.substring(aux.indexOf(texto), termina) + "</font> <font color=black>" + aux.substring(termina, aux.length()) + "</font></html>", i, 3);
                } else {
                    jTableCont.setValueAt("<html><font color=black>" + aux.substring(0, aux.indexOf(texto)) + "<html><font color=red>" + aux.substring(aux.indexOf(texto), aux.length()) + "</font></html>", i, 3);

                }
            }

        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed
    private void resetearColor() {
        for (int i = 0; i < jTableCont.getRowCount(); i++) {
            String cont = (String) jTableCont.getValueAt(i, 3);
            cont = cont.replaceAll("<html><font color=red>", "").replaceAll("</font></html>", "");
            jTableCont.setValueAt(cont, i, 3);
        }
    }


