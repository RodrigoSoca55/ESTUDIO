    private void BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrarActionPerformed
        // TODO add your handling code here:
        jLabelException.setVisible(false);
        String nombre = txtNombre.getText();
        String descripcion = txtAreaDesc.getText();
        txtNombre.setBorder(UIManager.getLookAndFeel().getDefaults().getBorder("TextField.border"));
        txtAreaDesc.setBorder(UIManager.getLookAndFeel().getDefaults().getBorder("TextField.border"));
        try {
            modelo.registrarTematica(nombre, descripcion);
            txtNombre.setText("");
            txtAreaDesc.setText("");
        } catch (Exception_StringVacio ex) {
            jLabelException.setVisible(true);
            jLabelException.setText("Complete las 치reas indicadas");
            Border border = BorderFactory.createLineBorder(Color.RED, 2);
            if (txtNombre.getText().equals("")) {
                txtNombre.setBorder(border);
            }
            if (txtAreaDesc.getText().equals("")) {
                txtAreaDesc.setBorder(border);
            }
        } catch (Exception_DuplicateName ex) {
            JOptionPane.showMessageDialog(null, "La tem치tica ya se encuentra registrada");
        }


    }//GEN-LAST:event_BtnRegistrarActionPerformed


